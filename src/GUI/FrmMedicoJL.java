    private void insertarDatos() {
        String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        String direccion = txtDireccion.getText();
        String especialidad = txtEspecialidad.getText();
        String experiencia = txtExperienciaM.getText();

        if (nombre.isEmpty() || edad.isEmpty() || direccion.isEmpty() || especialidad.isEmpty() || experiencia.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos deben ser llenados", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String query = "INSERT INTO medicojl (nombre, edad, direccion, especialidad, experiencia) VALUES (?, ?, ?, ?, ?)";

        try (Connection con = new Conexion("gestionmedica").getConexion(); PreparedStatement ps = con.prepareStatement(query)) {

            ps.setString(1, nombre);
            ps.setString(2, edad);
            ps.setString(3, direccion);
            ps.setString(4, especialidad);
            ps.setString(5, experiencia);

            int rowsAffected = ps.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Datos insertados exitosamente", "Exito", JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo insertar los datos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al insertar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limpiarCampos() {
        txtNombre.setText("");
        txtEdad.setText("");
        txtDireccion.setText("");
        txtEspecialidad.setText("");
        txtExperienciaM.setText("");
    }

    private void mostrarDatosEnTabla() {
        String query = "SELECT * FROM medicojl";

        DefaultTableModel modeloTabla = (DefaultTableModel) tablaMedicos.getModel();
        modeloTabla.setRowCount(0);

        try (Connection con = new Conexion("gestionmedica").getConexion(); PreparedStatement ps = con.prepareStatement(query); ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                Object[] fila = new Object[6];
                fila[0] = rs.getInt("id");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getInt("edad");
                fila[3] = rs.getString("direccion");
                fila[4] = rs.getString("especialidad");
                fila[5] = rs.getString("experiencia");

                modeloTabla.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

